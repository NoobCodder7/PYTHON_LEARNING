# Arithmetic operators 

a = 10
b = 5

print(a+b) # "+" is use to add two numbers
print(a-b) # "-" is use to subract two numbers
print(a*b) # "*" is used to multiply two numbers
print(a/b) # "/" is use to get quotient with decimal
print(a//b) # "//" is use to get quotient without decimal
print(a%b) # "%" is use to get remainde
print(a**b) # is use to get exponential 
